package com.christiantisby.firstkotlinprogram

fun main(args: Array<String>) {
    var number: Double = 7.6
    var isCheck: Boolean = true

    println("+number = "  + +number)
    println("-number = "  + -number)
    println("++number = "  + ++number)
    println("--number = "  + --number)
    println("!isCheck = "  + !isCheck)

}